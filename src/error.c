#include "cub3D.h"

int	error(char *msg)
{
	ft_putstr_fd("\e[31m\e[1mError\n", 2);
	ft_putstr_fd(msg, 2);
	ft_putstr_fd(" \e[0m \n", 2);
	exit (1);
	return (0);
}
